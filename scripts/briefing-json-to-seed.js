// scripts/briefing-json-to-seed.js
// One-time, reproducible seed: read data/briefing-data.json and emit
// migrations/0007_briefing_seed.sql as parameter-free INSERTs (updated_by = 'seed'
// so provenance is obvious). Single quotes are escaped by doubling.
//
// IDEMPOTENT-FRIENDLY: the generated SQL first DELETEs the owner's briefing rows
// (scoped to owner_email) then re-INSERTs them, so applying it twice is safe and
// only ever touches that one owner's data.
//
// JSON snapshots (calibration_snapshot / needs_attention / todays_meetings /
// personal_timeline) are stored as JSON TEXT; GET /api/briefing/me JSON.parses them.
//
// Usage:
//   node scripts/briefing-json-to-seed.js                 # repo data/ -> migrations/
//   node scripts/briefing-json-to-seed.js <in.json> <out.sql>
//
// IMPORTANT: writes the file itself as UTF-8 (matches scripts/json-to-seed.js); do
// NOT pipe via PowerShell `>` (that produces UTF-16 and corrupts multi-byte chars).

import { readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(__dirname, "..");

const inPath = process.argv[2]
  ? resolve(process.argv[2])
  : resolve(repoRoot, "data", "briefing-data.json");
const outPath = process.argv[3]
  ? resolve(process.argv[3])
  : resolve(repoRoot, "migrations", "0007_briefing_seed.sql");

// SQL literal for a possibly-null string. Returns NULL (unquoted) for null/undefined.
const s = (v) => (v == null ? "NULL" : `'${String(v).replace(/'/g, "''")}'`);
// SQL literal for a non-null string column (empty string allowed).
const sReq = (v) => `'${String(v ?? "").replace(/'/g, "''")}'`;
// SQL literal for an integer.
const n = (v) => String(Number(v) || 0);
// SQL literal for a JSON snapshot column (NULL when the value is null/undefined).
const jsonLit = (v) => (v == null ? "NULL" : `'${JSON.stringify(v).replace(/'/g, "''")}'`);

function build(data) {
  const owner = data.owner_email;
  if (!owner) throw new Error("briefing-data.json is missing owner_email");

  const lines = [];
  lines.push("-- migrations/0007_briefing_seed.sql");
  lines.push("-- Generated by scripts/briefing-json-to-seed.js from data/briefing-data.json.");
  lines.push("-- Do not hand-edit; re-run `npm run seed:briefing` to regenerate.");
  lines.push("-- updated_by = 'seed' marks the original import.");
  lines.push("-- Idempotent: deletes this owner's briefing rows, then re-inserts.");
  lines.push("");

  // --- Idempotent reset (scoped to this owner only) ---
  lines.push(`DELETE FROM briefing_items        WHERE owner_email = ${sReq(owner)};`);
  lines.push(`DELETE FROM briefing_project_refs WHERE owner_email = ${sReq(owner)};`);
  lines.push(`DELETE FROM briefing_state        WHERE owner_email = ${sReq(owner)};`);
  lines.push("");

  // --- briefing_state (one row) ---
  lines.push("-- Top-level briefing state (one row per owner).");
  lines.push(
    "INSERT INTO briefing_state " +
      "(owner_email, generated_at, calibration_snapshot, needs_attention, todays_meetings, updated_by) VALUES"
  );
  lines.push(
    `  (${sReq(owner)}, ${s(data.generated)}, ${jsonLit(data.calibration_snapshot)}, ` +
      `${jsonLit(data.needs_attention)}, ${jsonLit(data.todays_meetings)}, 'seed');`
  );
  lines.push("");

  // --- briefing_items (explicit ids; deterministic order) ---
  lines.push("-- Briefing section items.");
  let iid = 0;
  for (const it of data.items || []) {
    iid += 1;
    lines.push(
      "INSERT INTO briefing_items " +
        "(id, owner_email, section, text, meta, done, done_at, source, sort, updated_by) VALUES"
    );
    lines.push(
      `  (${iid}, ${sReq(owner)}, ${sReq(it.section)}, ${sReq(it.text)}, ${sReq(it.meta)}, ` +
        `${it.done ? 1 : 0}, ${s(it.done_at)}, ${s(it.source)}, ${n(it.sort)}, 'seed');`
    );
  }
  lines.push("");

  // --- briefing_project_refs (explicit ids; nullable project_id FK) ---
  lines.push("-- Personal project refs (compose shared projects via JOIN; never duplicate state).");
  let rid = 0;
  for (const r of data.project_refs || []) {
    rid += 1;
    lines.push(
      "INSERT INTO briefing_project_refs " +
        "(id, owner_email, project_id, personal_note, personal_timeline, sort, updated_by) VALUES"
    );
    lines.push(
      `  (${rid}, ${sReq(owner)}, ${r.project_id == null ? "NULL" : n(r.project_id)}, ` +
        `${s(r.personal_note)}, ${jsonLit(r.personal_timeline)}, ${n(r.sort)}, 'seed');`
    );
  }
  lines.push("");

  return lines.join("\n") + "\n";
}

const data = JSON.parse(readFileSync(inPath, "utf8"));
const sql = build(data);
writeFileSync(outPath, sql, "utf8");
console.error(`Wrote ${outPath} (${sql.length} bytes) from ${inPath}`);
console.error(
  `  owner ${data.owner_email}  items ${(data.items || []).length}  refs ${(data.project_refs || []).length}`
);
